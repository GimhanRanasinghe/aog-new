// Function to get station statistics
export function getStationStatistics() {
  const stats = stations.map((station) => {
    const aircraftAtStation = aircraftData.filter((aircraft) => aircraft.location === station.code)
    const aogCount = aircraftAtStation.filter((aircraft) => aircraft.status === "aog").length
    const maintenanceCount = aircraftAtStation.filter((aircraft) => aircraft.status === "maintenance").length
    const activeCount = aircraftAtStation.filter((aircraft) => aircraft.status === "active").length
    const scheduledCount = aircraftAtStation.filter((aircraft) => aircraft.status === "scheduled").length

    return {
      station: station.code,
      stationName: station.name,
      totalAircraft: aircraftAtStation.length,
      aogCount,
      maintenanceCount,
      activeCount,
      scheduledCount,
      aogPercentage: aircraftAtStation.length > 0 ? (aogCount / aircraftAtStation.length) * 100 : 0,
      maintenancePercentage: aircraftAtStation.length > 0 ? (maintenanceCount / aircraftAtStation.length) * 100 : 0,
      activePercentage: aircraftAtStation.length > 0 ? (activeCount / aircraftAtStation.length) * 100 : 0,
      scheduledPercentage: aircraftAtStation.length > 0 ? (scheduledCount / aircraftAtStation.length) * 100 : 0,
    }
  })

  return stats
}

